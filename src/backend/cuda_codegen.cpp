CUDAHostCodegen::CUDAHostCodegen(const CUDACodegenConfig& config)
    : config_(config), indent_level_(0) {}

std::string CUDAHostCodegen::generate_host_wrapper(
    const tensorir::TensorOp& op,
    const CUDAKernelMeta& kernel_meta) {
    
    code_.str("");
    code_.clear();
    indent_level_ = 0;
    
    emit_line("// Host wrapper for " + op.name);
    emit_line("extern \"C\" void " + op.name + "_cuda(");
    increase_indent();
    
    std::vector<std::string> all_bufs;
    for (const auto* buf : op.inputs) {
        if (buf) {
            std::string line = get_element_type(buf) + "* " + buf->name;
            line += ",";
            emit_line(line);
            all_bufs.push_back(buf->name);
        }
    }
    for (const auto* buf : op.outputs) {
        if (buf) {
            bool already = false;
            for (const auto* in : op.inputs) {
                if (in && in->name == buf->name) {
                    already = true;
                    break;
                }
            }
            if (!already) {
                std::string line = get_element_type(buf) + "* " + buf->name;
                line += ",";
                emit_line(line);
                all_bufs.push_back(buf->name);
            }
        }
    }
    decrease_indent();
    emit_line(") {");
    increase_indent();
    
    emit_line("// Device memory allocation");
    for (const auto& name : all_bufs) {
        generate_device_alloc(name, "float", 1024 * 1024 * sizeof(float));
    }
    emit_line("");
    
    emit_line("// Copy data to device");
    for (const auto& name : all_bufs) {
        generate_memcpy_h2d(name, "d_" + name, 1024 * 1024 * sizeof(float));
    }
    emit_line("");
    
    emit_line("// Launch kernel");
    std::vector<std::string> device_args;
    for (const auto& name : all_bufs) {
        device_args.push_back("d_" + name);
    }
    generate_kernel_launch(kernel_meta, device_args);
    emit_line("");
    
    emit_line("// Copy result back to host");
    if (!all_bufs.empty()) {
        generate_memcpy_d2h("d_" + all_bufs[0], all_bufs[0], 1024 * 1024 * sizeof(float));
    }
    emit_line("");
    
    emit_line("// Free device memory");
    for (const auto& name : all_bufs) {
        generate_device_free("d_" + name);
    }
    
    decrease_indent();
    emit_line("}");
    emit_line("");
    
    return code_.str();
}

std::string CUDAHostCodegen::generate_memory_management(const tensorir::TensorOp& op) {
    (void)op;
    std::stringstream local_code;
    int local_indent = 0;
    
    auto emit = [&](const std::string& line) {
        for (int i = 0; i < local_indent; ++i) {
            local_code << "  ";
        }
        local_code << line << "\n";
    };
    
    emit("// Memory management utilities");
    emit("template<typename T>");
    emit("T* cuda_alloc(size_t count) {");
    local_indent++;
    emit("T* ptr = nullptr;");
    emit("cudaMalloc(&ptr, count * sizeof(T));");
    emit("return ptr;");
    local_indent--;
    emit("}");
    emit("");
    
    emit("template<typename T>");
    emit("void cuda_free(T* ptr) {");
    local_indent++;
    emit("cudaFree(ptr);");
    local_indent--;
    emit("}");
    emit("");
    
    return local_code.str();
}

std::string CUDAHostCodegen::generate_host_module(
    const tensorir::TensorIRModule& module,
    const std::vector<CUDAKernelMeta>& kernel_metas) {
    
    code_.str("");
    code_.clear();
    indent_level_ = 0;
    
    emit_line("#include <cuda_runtime.h>");
    emit_line("#include <cuda_runtime_api.h>");
    emit_line("#include <device_launch_parameters.h>");
    emit_line("#include <cstdio>");
    emit_line("#include <cstdlib>");
    emit_line("");
    
    generate_error_check_macro();
    emit_line("");
    
    code_ << generate_memory_management(tensorir::ComputeOp(""));
    
    for (const auto& meta : kernel_metas) {
        emit_line("__global__ void " + meta.kernel_name + "(");
        increase_indent();
        for (size_t i = 0; i < meta.arg_names.size(); ++i) {
            std::string line = meta.arg_types[i] + " " + meta.arg_names[i];
            if (i < meta.arg_names.size() - 1) line += ",";
            emit_line(line);
        }
        decrease_indent();
        emit_line(");");
    }
    emit_line("");
    
    return code_.str();
}

void CUDAHostCodegen::emit_line(const std::string& line) {
    emit_indent();
    code_ << line << "\n";
}

void CUDAHostCodegen::emit_indent() {
    for (int i = 0; i < indent_level_; ++i) {
        code_ << "  ";
    }
}

void CUDAHostCodegen::generate_error_check_macro() {
    emit_line("#define CUDA_CHECK(call) \\");
    emit_line("  do { \\");
    emit_line("    cudaError_t err = call; \\");
    emit_line("    if (err != cudaSuccess) { \\");
    emit_line("      fprintf(stderr, \"CUDA error at %s:%d: %s\\n\", \\");
    emit_line("              __FILE__, __LINE__, cudaGetErrorString(err)); \\");
    emit_line("      exit(EXIT_FAILURE); \\");
    emit_line("    } \\");
    emit_line("  } while(0)");
}

void CUDAHostCodegen::generate_device_alloc(const std::string& name,
                                              const std::string& type,
                                              size_t size) {
    (void)type;
    emit_line("float* d_" + name + " = nullptr;");
    emit_line("CUDA_CHECK(cudaMalloc(&d_" + name + ", " + std::to_string(size) + "));");
}

void CUDAHostCodegen::generate_memcpy_h2d(const std::string& host_ptr,
                                            const std::string& device_ptr,
                                            size_t size) {
    emit_line("CUDA_CHECK(cudaMemcpy(" + device_ptr + ", " + host_ptr + ", " +
              std::to_string(size) + ", cudaMemcpyHostToDevice));");
}

void CUDAHostCodegen::generate_memcpy_d2h(const std::string& device_ptr,
                                            const std::string& host_ptr,
                                            size_t size) {
    emit_line("CUDA_CHECK(cudaMemcpy(" + host_ptr + ", " + device_ptr + ", " +
              std::to_string(size) + ", cudaMemcpyDeviceToHost));");
}

void CUDAHostCodegen::generate_kernel_launch(const CUDAKernelMeta& meta,
                                              const std::vector<std::string>& arg_names) {
    emit_line("dim3 block(" + std::to_string(meta.block_dim_x) + ", " +
              std::to_string(meta.block_dim_y) + ", " +
              std::to_string(meta.block_dim_z) + ");");
    emit_line("dim3 grid(" + std::to_string(meta.grid_dim_x) + ", " +
              std::to_string(meta.grid_dim_y) + ", " +
              std::to_string(meta.grid_dim_z) + ");");
    
    if (meta.shared_memory_size > 0) {
        emit_line("size_t shared_mem = " + std::to_string(meta.shared_memory_size) + ";");
    }
    
    std::string launch = meta.kernel_name + "<<<grid, block";
    if (meta.shared_memory_size > 0) {
        launch += ", shared_mem";
    }
    launch += ">>>(";
    
    for (size_t i = 0; i < arg_names.size(); ++i) {
        if (i > 0) launch += ", ";
        launch += arg_names[i];
    }
    launch += ");";
    
    emit_line(launch);
    emit_line("CUDA_CHECK(cudaDeviceSynchronize());");
}

void CUDAHostCodegen::generate_device_free(const std::string& name) {
    emit_line("CUDA_CHECK(cudaFree(" + name + "));");
}

std::string CUDAHostCodegen::get_element_type(const tensorir::Buffer* buf) const {
    if (!buf) return "float";
    if (buf->elem_type == "f32") return "float";
    if (buf->elem_type == "f64") return "double";
    if (buf->elem_type == "i32") return "int";
    if (buf->elem_type == "i64") return "int64_t";
    if (buf->elem_type == "u32") return "uint32_t";
    if (buf->elem_type == "u8") return "uint8_t";
    return "float";
}

// ============================================================================
// CUDACodeGenerator 实现
// ============================================================================

CUDACodeGenerator::CUDACodeGenerator(const CUDACodegenConfig& config)
    : config_(config), kernel_codegen_(config), host_codegen_(config) {}

std::string CUDACodeGenerator::generate_module(
    const tensorir::TensorIRModule& module) {
    
    std::string result;
    kernel_metas_.clear();
    
    result += host_codegen_.generate_host_module(module, {});
    
    for (const auto& op : module.operations) {
        if (op) {
            auto sched_it = module.schedules.find(op.get());
            tensorir::Schedule default_sched(op.get());
            const tensorir::Schedule* sched = &default_sched;
            if (sched_it != module.schedules.end() && sched_it->second) {
                sched = sched_it->second.get();
            }
            
            std::string kernel = kernel_codegen_.generate_kernel(*op, *sched);
            result += kernel;
            result += "\n";
            kernel_metas_.push_back(kernel_codegen_.get_kernel_meta());
            
            std::string host_wrapper = host_codegen_.generate_host_wrapper(*op, kernel_codegen_.get_kernel_meta());
            result += host_wrapper;
            result += "\n";
        }
    }
    
    return result;
}

std::string CUDACodeGenerator::generate_kernel_with_host(
    const tensorir::TensorOp& op,
    const tensorir::Schedule& schedule) {
    
    std::string result;
    result += "#include <cuda_runtime.h>\n";
    result += "#include <cuda_runtime_api.h>\n";
    result += "#include <device_launch_parameters.h>\n\n";
    
    result += kernel_codegen_.generate_kernel(op, schedule);
    result += "\n";
    result += host_codegen_.generate_host_wrapper(op, kernel_codegen_.get_kernel_meta());
    
    return result;
}

