std::string generate_cuda_code(const tensorir::TensorOp& op,
                                const tensorir::Schedule& schedule,
                                const CUDACodegenConfig& config) {
    CUDACodeGenerator generator(config);
    return generator.generate_kernel_with_host(op, schedule);
}

std::string generate_cuda_launch_code(const CUDAKernelMeta& meta,
                                       const std::vector<std::string>& arg_names) {
    std::stringstream ss;
    ss << "dim3 block(" << meta.block_dim_x << ", " << meta.block_dim_y << ", " << meta.block_dim_z << ");\n";
    ss << "dim3 grid(" << meta.grid_dim_x << ", " << meta.grid_dim_y << ", " << meta.grid_dim_z << ");\n";
    
    ss << meta.kernel_name << "<<<grid, block";
    if (meta.shared_memory_size > 0) {
        ss << ", " << meta.shared_memory_size;
    }
    ss << ">>>(";
    
    for (size_t i = 0; i < arg_names.size(); ++i) {
        if (i > 0) ss << ", ";
        ss << arg_names[i];
    }
    ss << ");\n";
    ss << "cudaDeviceSynchronize();\n";
    
    return ss.str();
}

